/// Generate standalone DESIGN-DEEP-DIVES.md from per-crate DEEPDIVE_*.md.tmpl files.
pub fn generate_deep_dives_md(cfg: &Config) -> String {
    let header = generation_header_md(cfg);
    let dives = collect_deep_dives(&cfg.mock_dir);
    if dives.is_empty() {
        return String::new();
    }

    let name = &cfg.project_name;
    let mut md = String::new();
    writeln!(md, "{header}").unwrap();
    writeln!(md, "# {name} — Deep Dives").unwrap();
    writeln!(md).unwrap();
    writeln!(md, "> Detailed technical deep dives into {name} subsystems.").unwrap();
    writeln!(md, "> See also: [DESIGN.md](DESIGN.md) for the high-level design.").unwrap();
    writeln!(md).unwrap();
    writeln!(md, "---").unwrap();
    writeln!(md).unwrap();
    writeln!(md, "{dives}").unwrap();

    md
}

/// Compute crate summaries from per-crate README.md.tmpl files.
fn compute_crate_summaries(mock_dir: &Path) -> String {
    let crates_dir = mock_dir.join("crates");
    if !crates_dir.is_dir() {
        return String::new();
    }

    let mut crate_dirs: Vec<_> = fs::read_dir(&crates_dir)
        .unwrap()
        .filter_map(|e| e.ok())
        .filter(|e| e.path().is_dir())
        .collect();
    crate_dirs.sort_by_key(|e| e.file_name());

    let mut summaries = String::new();

    for crate_entry in crate_dirs {
        let crate_path = crate_entry.path();
        let crate_name = crate_entry.file_name().to_string_lossy().to_string();
        let readme_path = crate_path.join("README.md.tmpl");

        if let Ok(content) = fs::read_to_string(&readme_path) {
            // Insert the README content (already short, 3-10 lines)
            writeln!(summaries, "{}", content.trim()).unwrap();

            // Add links to overview and deep dives if they exist
            let has_design = crate_path.join("DESIGN.md.tmpl").exists();
            let deep_dives = find_deep_dives(&crate_path);

            if has_design || !deep_dives.is_empty() {
                writeln!(summaries).unwrap();
                let crate_upper = crate_name.to_uppercase().replace('-', "_");
                if has_design {
                    writeln!(summaries, "- [Overview]({crate_upper}_OVERVIEW.md)").unwrap();
                }
                for (subject, _) in &deep_dives {
                    let subject_upper = subject.to_uppercase();
                    writeln!(summaries, "- [{subject} deep dive]({crate_upper}_{subject_upper}.md)").unwrap();
                }
            }

            writeln!(summaries).unwrap();
        }
    }

    summaries.trim_end().to_string()
}

/// Find DEEPDIVE_*.md.tmpl files in a crate directory.
/// Returns (subject_name, full_path) pairs.
fn find_deep_dives(crate_path: &Path) -> Vec<(String, std::path::PathBuf)> {
    let mut dives = Vec::new();
    if let Ok(entries) = fs::read_dir(crate_path) {
        for entry in entries.flatten() {
            let name = entry.file_name().to_string_lossy().to_string();
            if name.starts_with("DEEPDIVE_") && name.ends_with(".md.tmpl") {
                let subject = name
                    .trim_start_matches("DEEPDIVE_")
                    .trim_end_matches(".md.tmpl")
                    .to_string();
                dives.push((subject, entry.path()));
            }
        }
    }
    dives.sort_by(|a, b| a.0.cmp(&b.0));
    dives
}

/// Generate per-crate overview and deep dive files in docs/.
pub fn generate_per_crate_docs(cfg: &Config) {
    if !cfg.crates_dir.is_dir() {
        return;
    }

    let header = generation_header_md(cfg);

    let mut crate_dirs: Vec<_> = fs::read_dir(&cfg.crates_dir)
        .unwrap()
        .filter_map(|e| e.ok())
        .filter(|e| e.path().is_dir())
        .collect();
    crate_dirs.sort_by_key(|e| e.file_name());

    let mut count = 0;

    for crate_entry in crate_dirs {
        let crate_path = crate_entry.path();
        let crate_name = crate_entry.file_name().to_string_lossy().to_string();
        let crate_upper = crate_name.to_uppercase().replace('-', "_");

        // Generate <CRATE>_OVERVIEW.md from DESIGN.md.tmpl
        let design_path = crate_path.join("DESIGN.md.tmpl");
        if let Ok(content) = fs::read_to_string(&design_path) {
            let out_name = format!("{crate_upper}_OVERVIEW.md");
            let out_path = cfg.docs_dir.join(&out_name);
            let full = format!("{header}\n{content}");
            fs::write(&out_path, &full).expect("failed to write overview");
            count += 1;
        }

        // Generate <CRATE>_<SUBJECT>.md from DEEPDIVE_*.md.tmpl
        for (subject, dd_path) in find_deep_dives(&crate_path) {
            if let Ok(content) = fs::read_to_string(&dd_path) {
                let subject_upper = subject.to_uppercase();
                let out_name = format!("{crate_upper}_{subject_upper}.md");
                let out_path = cfg.docs_dir.join(&out_name);
                let full = format!("{header}\n{content}");
                fs::write(&out_path, &full).expect("failed to write deep dive");
                count += 1;
            }
        }
    }

    eprintln!("  generated {count} per-crate doc files");
}
